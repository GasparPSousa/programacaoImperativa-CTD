// Switch

let cincoOuDezAnos = function(idade) {
    
    // Criando o switch dentro da função
    switch (idade) {
        case 10:
            console.log('Tem 10 anos.')
            break;
        case 5:
            console.log('Tem 5 anos.')
            break
        default:
            console.log('Tem idade diferente de 5 ou 10 anos.')
    
    }
}

cincoOuDezAnos(5)
cincoOuDezAnos(7)
cincoOuDezAnos(10)
cincoOuDezAnos(15)


