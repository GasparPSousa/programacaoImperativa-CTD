produtos = ["macarrão", "carne", "ovos", "bacon", "feijão"]

console.log(produtos)
console.log()

/*
Falar sobre os métodos:

- Join
- Pop
- Push
- Shift
- Unshift

*/

console.log("O método join() junta todos elementos do array em um só.")
console.log(produtos.join("-"))
console.log()

console.log("O método pop() retira o último elemento do array")
console.log(produtos.pop())
console.log(produtos)
console.log()

console.log("O método push() adiciona um ou mais elementos ao final do array")
console.log(produtos.push("feijão", "peixe"))
console.log(produtos)
console.log()

console.log("O método shift() retira o primeiro elemento do array")
console.log(produtos.shift())
console.log(produtos)
console.log()

console.log("O método unshift() adiciona um ou mais elementos no início do array")
console.log(produtos.unshift("maça", "acabate"))
console.log(produtos)
console.log()


